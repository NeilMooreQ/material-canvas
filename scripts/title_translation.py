TOKEN_RE = re.compile(r"[A-Za-z]+(?:'[A-Za-z]+)?|\d+|[А-Яа-яЁё]+|[^\w\s]", re.UNICODE)


def _apply_phrases(value: str) -> str:
    result = value.lower()
    for phrase, replacement in sorted(PHRASE_MAP.items(), key=lambda item: len(item[0]), reverse=True):
        pattern = r"\b" + re.escape(phrase) + r"\b"
        result = re.sub(pattern, f" {replacement} ", result)
    return result


def _join_tokens(tokens: list[str]) -> str:
    text = " ".join(token for token in tokens if token)
    text = re.sub(r"\s+([,.:;!?])", r"\1", text)
    text = re.sub(r"([(-])\s+", r"\1", text)
    text = re.sub(r"\s+([)])", r"\1", text)
    text = re.sub(r"\s*-\s*", "-", text)
    text = re.sub(r"\s+", " ", text).strip(" -")
    return text


def translate_title(title: str) -> str:
    prepared = _apply_phrases(title)
    translated = []
    for token in TOKEN_RE.findall(prepared):
        lower = token.lower()
        if re.fullmatch(r"[A-Za-z]+(?:'[A-Za-z]+)?", token):
            translated.append(WORD_MAP.get(lower, token))
        else:
            translated.append(token)

    result = _join_tokens(translated)
    return result[:1].upper() + result[1:] if result else title
